### Type Hints

my_string = "Hola Mundo"
print(my_string)
print(type(my_string))

my_string = 22
print(my_string)
print(type(my_string))

my_typed_variable: str = "Mi tipado es cadena de texto"
print(my_typed_variable)
print(type(my_typed_variable))


my_typed_variable = 5
print(my_typed_variable)
print(type(my_typed_variable))
